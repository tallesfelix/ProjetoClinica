package modeloBeans;

/**
 * Classe que representa um Paciente no dominio da aplicação.
 *
 * @author Allan Gomes
 * @author Daniel Nunes
 * @author Luis Eduardo
 * @author Talles Felix
 */

public class BeansPacientes {

    /**
     * Codigo do Paciente
     */
    private int codPac;

    /**
     * nome do Paciente
     */
    private String nomePac;

    /**
     * data de nascimento do Paciente
     */
    private String nasc;

    /**
     * RG do Paciente
     */
    private String rg;

    /**
     * Telefone do Paciente
     */
    private String telefone;

    /**
     * Rua do Paciente
     */
    private String rua;

    /**
     * Complemnto do endereço do Paciente
     */
    private String complemento;

    /**
     * CEP do Paciente
     */
    private String cep;

    /**
     * Bairro do Paciente
     */
    private String nomeBairro;

    /**
     * Pesquisa
     */
    private String pesquisa;

    /**
     * @return the codPac
     */
    public int getCodPac() {
        return codPac;
    }

    /**
     * @param codPac the codPac to set
     */
    public void setCodPac(int codPac) {
        this.codPac = codPac;
    }

    /**
     * @return the nomePac
     */
    public String getNomePac() {
        return nomePac;
    }

    /**
     * @param nomePac the nomePac to set
     */
    public void setNomePac(String nomePac) {
        this.nomePac = nomePac;
    }

    /**
     * @return the nasc
     */
    public String getNasc() {
        return nasc;
    }

    /**
     * @param nasc the nasc to set
     */
    public void setNasc(String nasc) {
        this.nasc = nasc;
    }

    /**
     * @return the rg
     */
    public String getRg() {
        return rg;
    }

    /**
     * @param rg the rg to set
     */
    public void setRg(String rg) {
        this.rg = rg;
    }

    /**
     * @return the telefone
     */
    public String getTelefone() {
        return telefone;
    }

    /**
     * @param telefone the telefone to set
     */
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    /**
     * @return the rua
     */
    public String getRua() {
        return rua;
    }

    /**
     * @param rua the rua to set
     */
    public void setRua(String rua) {
        this.rua = rua;
    }

    /**
     * @return the complemento
     */
    public String getComplemento() {
        return complemento;
    }

    /**
     * @param complemento the complemento to set
     */
    public void setComplemento(String complemento) {
        this.complemento = complemento;
    }

    /**
     * @return the cep
     */
    public String getCep() {
        return cep;
    }

    /**
     * @param cep the cep to set
     */
    public void setCep(String cep) {
        this.cep = cep;
    }

    /**
     * @return the nomeBairro
     */
    public String getNomeBairro() {
        return nomeBairro;
    }

    /**
     * @param nomeBairro the nomeBairro to set
     */
    public void setNomeBairro(String nomeBairro) {
        this.nomeBairro = nomeBairro;
    }

    /**
     * @return the pesquisa
     */
    public String getPesquisa() {
        return pesquisa;
    }

    /**
     * @param pesquisa the pesquisa to set
     */
    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }

}
